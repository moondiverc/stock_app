class Profile {
  final String name;
  final String age;
  final String major;
  final String job;
  final String location;
  final String status;
  final String description;
  final String profileImage;

  const Profile({
    required this.name,
    required this.age,
    required this.major,
    required this.job,
    required this.location,
    required this.status,
    required this.description,
    required this.profileImage,
  });
}
